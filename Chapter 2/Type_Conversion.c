#include <stdio.h>

int main(){
// OPERATION Between :
// *1 int and int     = int 
// *2 int and float   = float
// *3 float and float = float



// *1 int and int     = int  
    int a = 9;
    int b = 2 ;
    float c = a/b;
    printf("The value of a/b = %d", c);
   

// *2 int and float   = float
    int a = 9;
    float b = 2 ;
    float c = a/b;
    printf("The value of a/b = %f", c);

// *3 float and float = float
    float a = 9;
    float b = 2 ;
    float c = a/b;
    printf("The value of a/b = %f", c);


}

